    private void analizarTokens() throws FileNotFoundException, IOException{
        
        Iterable<Tabla> tokenLista = new LinkedList<>();
        this.jTextArea1.selectAll();
        this.jTextArea1.replaceSelection("");
        int contadorID=0;
        File fichero= new File("Fuente.txt");
        PrintWriter writer;
        writer= new PrintWriter(fichero);
        writer.write(jTextField1.getText());
        writer.close();
        Reader leer = new BufferedReader( new FileReader("Fuente.txt"));
        Lexico lexico = new Lexico(leer);
        String resultado = "";
        while (true) { 
            Tokens token = lexico.yylex();
            if(token==null){
                for (Tabla tabla : tokenLista) {
                    System.out.println(tabla.getNombre()
                            +"="+tabla.getId());
                }
                jTextArea1.append(resultado);
                return;
            }
            
            switch(token){
                case ParentIzq:
                    resultado+="<(>";
                    break;
                case ParentDer:
                    resultado+="<)>";
                    break;
                case verdadero:
                    resultado+="<true>";
                    break;
                case TRUE:
                    resultado+="<TRUE>";
                    break;
                case falso:
                    resultado+="<false>";
                    break;
                case FALSE:
                    resultado+="<FALSE>";
                    break;
                case not:
                    resultado+="<not>";
                    break; 
                case NOT:
                    resultado+="<NOT>";
                    break;    
                case or:
                    resultado+="<or>";
                    break;
                case OR:
                    resultado+="<OR>";
                    break;
                case and:
                    resultado+="<and>";
                    break;
                case AND:
                    resultado+="<AND>";
                    break;
                case si:
                    resultado+="<sii>";
                    break;
                case SI:
                    resultado+="<SII>";
                    break;
                case sii:
                    resultado+="<sii>";
                    break;
                case SII:
                    resultado+="<SII>";
                    break;
                case ERROR:
                    resultado+="<Error, simbolo no encontrado>";
                    break;
                case ID:
                    contadorID++;
                    Tabla tokens = new Tabla();
                    tokens.setNombre(lexico.yytext());
                    tokens.setId(contadorID);
                    tokenLista.add(tokens);
                    resultado+="<ID"+contadorID+">";
                    break;
                case INT:
                    resultado+="<"+lexico.yytext()+">";
                    break;
                default:
                    resultado+="<"+lexico.yytext()+">";
            }
            
        }//To change body of generated methods, choose Tools | Templates.
    }
